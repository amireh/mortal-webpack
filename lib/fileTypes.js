/**
 * @module FileTypes
 */

/** @property {RegExp} */
exports.JS = /\.js$/;

/** @property {RegExp} */
exports.JSON = /\.json$/;

/** @property {RegExp} */
exports.JSX = /\.jsx$/;

/** @property {RegExp} */
exports.Script = /\.jsx?$/;

/** @property {RegExp} */
exports.CSS = /\.css$/;

/** @property {RegExp} */
exports.LESS = /\.less$/;

/** @property {RegExp} */
exports.SASS = /\.s[ac]ss$/;

/** @property {RegExp} */
exports.StyleSheet = /\.(css|less|s[ac]ss)$/

/** @property {RegExp} */
exports.Font = /\.(svg|eot|otf|ttf|woff2?)$/

/** @property {RegExp} */
exports.SVG = /\.svg$/

/** @property {RegExp} */
exports.EOT = /\.eot$/

/** @property {RegExp} */
exports.OTF = /\.otf$/

/** @property {RegExp} */
exports.TTF = /\.ttf$/

/** @property {RegExp} */
exports.WOFF = /\.woff2?$/

exports.PNG = /\.png\b/
exports.GIF = /\.gif\b/
exports.JPEG = /\.jpe?g\b/
exports.Image = /\.(png|gif|jpe?g|svg)\b/